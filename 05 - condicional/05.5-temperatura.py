# Criando a variável
temperatura = float(input("Digite a temperatura em Celsius: "))

# Verificando a condição da temperatura
if temperatura < 10:
    print("Está muito frio!")
elif temperatura < 20:
    print("Está frio!")
elif temperatura < 30:
    print("Está agradável")
else:
    print("Está muito quente!")
   

    
